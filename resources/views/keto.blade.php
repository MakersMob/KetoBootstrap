				<h2 id="body">What Happens to My Body?</h2>

				<p>Because you're completely rewiring how your body works, your body isn't going to be ready right away to handle the breakdown of fats for energy.</p>

				<p>While switching over to keto you'll have a transition period where your body uses up all of its glycogen stores and doesn't have enough enzymes to breakdown fat to produce ketones.</p>

				<p>This means your body doesn't have an immediate fuel source which causes a lack of energy and general lethargy.</p>

				<p>In the first week of keto, many people will report headaches, mental fogginess, dizziness, and aggravation. Sounds terrible, right?</p>

				<p>This is caused by the loss of electrolytes so it's important that you continue to replace them throughout the day. Keeping your sodium (don't hesitate to salt things up) intake up throughout the day can prevent all of these side effects.</p>

				<p>Sodium helps with water retention in the body along with replenishing the much needed electrolytes.</p>

				<p>The groggy feeling and fatigue actually has a term and it's <em>keto flu</em>.</p>

