				<p>When transitioning to keto, you may feel some slight discomfort along with fatigue, headaches, nausea and cramps. It doesn't sound fun but it's important to understand why it is happening.</p>

				<ul>
					<li><strong>Keto is a diuretic.</strong> Everytime you urinate you're losing electrolytes and water. To combat this you can make a nice drink from a bouillion cube (makes a great broth) or by using <a href="http://amzn.to/2xqN5AD" rel="nofollow">MiO with Electrolytes</a> and increasing your water intake. The goal is to replace the electrolytes that you're using.</li>
					<li><strong>You're transitioning.</strong> All of the years of carb intake has trained your body to convert carbs into glycogen so when you transition over to keto, your body needs time to make the proper adjustments. You can't simply make your car go electric by adding another battery.</li>
				</ul>

				<p>For most people, eating less than 20g of net carbs a day will get them on track for ketosis within a matter of days. To ensure the process is sped up some you should aim for less than 15g of net carbs daily.</p>

				<p>Continue to monitor your electrolyte intake along with how much water you are drinking.</p>

				<p>To learn how to beat the Keto Flu go on and read <a href="https://thrivestrive.com/keto-flu/" target="_blank"><em>Keto Flu: What It Is and How to Beat It the Healthy Way</em></a>.</p>

				<h2 id="effects">Common Side Effects on a Keto Diet</h2>

				<p>As with any drastic change you make to your body's chemistry there are going to be side effects. Of course, if you think about it, your current way of eating has side effects as well.</p>

				<p>On keto there are known ways to combat each of these side effects so you're in good hands.</p>

				<h3>Cramps</h3>

				<p>Do to keto being a diuretic, when your body is losing out on fluids it can cause cramps.</p>

				<p>To prevent these you do the same thing that you're already doing to prevent keto flu and that is upping your water and sodium intake. If you find that cramps still persist then you might look into taking a <a href="http://amzn.to/2yQIMuW" rel="nofollow">magnesium supplement</a>.</p>

				<h3>Constipation</h3>

				<p>Because the most common cause of constipation is dehydration, you can help prevent it by increasing the amount of water you drink everyday.</p>

				<p>You also want to ensure that the vegetables you eat contain quality fiber.</p>

				<p>If you find that these aren't enough then you can add <a href="http://amzn.to/2xpIvmx" rel="nofollow">psyllium husk powder</a> to your drinks and meals.</p>

				<h3>Heart Palpitations</h3>

				<p>This sounds scarier than it really is. Your heart may begin to beat faster and harder when transitioning over to keto, it's pretty standard.</p>

				<p>If you the problem persists over a long period of time then you need to make sure you're drinking enough water and eating enough salt.</p>

				<p>If the problem continues to persist then you may need to add a <a href="http://amzn.to/2y9tS5z" rel="nofollow">potassium supplement</a> to the mix.</p>

				<h3>Reduced Physical Performance</h3>

				<p>Since your body hasn't fully transitioned to burning fat yet, it loses out on its energy source pretty quickly if you are exercising hard.</p>

				<p>As your body shifts to using fat for energy, you'll find that all of your strength and endurance will return to normal.</p>

