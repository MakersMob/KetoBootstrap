				<p>When it comes to sticking to a diet one of the things that you have to keep an eye on is what you eat.</p>

<p>In a sense that is the easy part.</p>
<p>Don't drink 5 milkshakes a day.</p>
<p>However, an even more important part of working with a diet is making sure that your body is getting the right nutrition that it needs so that it can do what you want it to do.</p>
<p>While there are a ton of micronutrients that your body wants (sodium, potassium, etc.) the things that you will need to track every single day to ensure your success is your macros.</p>

<p><strong id="docs-internal-guid-64cc4b6b-6e34-57ab-dec7-35bf2afa3d71">What Are Macros?</strong></p>

<p>Your macros are:</p>

<ul>
	<li>Protein</li>
	<li>Fat</li>
	<li>Carbohydrates</li>
</ul>

<p>Every day when you plan your meals, they must revolve around getting the right amount of macros into your system so that your body has a chance of succeeding.</p>
&nbsp;

<p><strong id="docs-internal-guid-64cc4b6b-6e34-57ab-dec7-35bf2afa3d71">Finding Out Your Macro Requirements</strong></p>

<p>If you're going to track your macros you need to know the numbers that you need to track.</p>
In general, depending on your body (height and weight) and your lifestyle (sedentary or active), your body has different caloric requirements.&nbsp;

<p>For the sake of simplicity let's say you need 2,000 calories a day for your body to run at an optimal level.</p>

<p>The general ratio of macros in the Keto diet is fat 75%, protein 20%, and carbs 5%.</p>

<p>That means your daily macro numbers look like this:</p>

<ul>
	<li>75% of your calories come from fats</li>
	<li>20% of your calories come from protein</li>
	<li>5% of your calories come from carbs</li>
</ul>
So the question you might be asking yourself is how do I figure out my macros? Well, thats simple just send me the following info:<br />
<br />
Gender<br />
Age<br />
Height<br />
Weight&nbsp;<br />
Activity Level&nbsp;
<ul>
	<li>No Exercise Low Activity</li>
	<li>Exercise 1-2x week Low Intensity-casual walk, a stretch session yoga class or tai chi, bike riding or using an elliptical or spin bike at an easy pace</li>
	<li>Exercise 1-2x week Mid/High Intensity-Moderate-intensity exercise weight training, or endurance exercises-gym classes, jogging, cycling, or lap swimming.</li>
	<li>Exercise 3-4x week Low Intensity</li>
	<li>Exercise 3-4x week Mid/High Intensity</li>
	<li>Exercise more than 4x week Low Intensity</li>
	<li>Exercise more than 4x week Mid/High Intensity</li>
	<li>High Intensity Exercise more than 5x week-HITT</li>
</ul>
&nbsp;

<p><strong id="docs-internal-guid-64cc4b6b-6e34-57ab-dec7-35bf2afa3d71">How to Track Macros</strong>&nbsp;</p>

<p>The easiest way to track your macros is with an app on your phone. The app that I use to track what I'm eating throughout the day is My Macros+. MyFitnessPal is another great option. With that said&nbsp;feel free to use any app you like.</p>

<p>The interface isn't the most intuitive in the world but My Macros+&nbsp;does an amazing job of helping me plan my meals and keep me on track. After your macros are calculated, My Macros+ or MyFitnessPal make&nbsp;it easy for you to add food, create recipes, and to track each meal. These apps also takes all the guess work out of how many macros you have left after adding in a meal, how close you are to hitting your macros and if you’ve hit your macros for the day.&nbsp;</p>

<p>If you always want to have your macros handy when you're on KetoDash we make it easy to see them at the top of the screen. Just edit your profile to add your already calculated macros to your profile. You will need to add your macros in as a decimal. For example, if your fat macro is 112 you will need to input it as 112.0.</p>

<p>When it comes to planning out your macros, I’ve found with time that the less stress, the better. That being said, the best piece of advice that I can give you is this: plan your meals in advance!</p>
